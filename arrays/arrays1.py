# Crear una lista
numeros = [1, 2, 3, 4, 5]

# Acceso
print(numeros[0])   # Primer elemento
print(numeros[-1])  # Último elemento

# Operaciones
print(len(numeros))       # Longitud
print(sum(numeros))       # Suma
print(max(numeros))       # Máximo
print(min(numeros))       # Mínimo

# Rebanado (slicing)
print(numeros[1:4])  # [2, 3, 4]